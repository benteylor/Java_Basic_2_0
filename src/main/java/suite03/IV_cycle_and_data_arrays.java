package suite03;

public class IV_cycle_and_data_arrays {

    public static void main(String[] args) {

        short[] nums = { 0, 2, 4, 6, 8, 10};

        for (int i = 0; i < nums.length; i++) { // Таким образом мы указываем количество элементов массива (от нуля, до последнего)
            System.out.println(nums[i]);


        }







    }
}
